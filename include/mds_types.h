#ifndef MDS_TYPES_H_
#define MDS_TYPES_H_

#include <ruts/ints.h>

namespace mds {
  namespace ints = ruts::ints;
  using bool_t =   bool;
  using byte_t =   ints::int_t<8>::exact;   // std::int8_t
  using ubyte_t =  ints::uint_t<8>::exact;  // std::uint8_t
  using short_t =  ints::int_t<16>::exact;  // std::int16_t
  using ushort_t = ints::uint_t<16>::exact; // std::uint16_t
  using int_t =    ints::int_t<32>::exact;  // std::int32_t
  using uint_t =   ints::uint_t<32>::exact; // std::uint32_t
  using long_t =   ints::int_t<64>::exact;  // std::int64_t
  using ulong_t =  ints::uint_t<64>::exact; // std::uint64_t
  using float_t =  float;
  using double_t = double;
  using char_t = char16_t;

}



#endif /* MDS_TYPES_H_ */
