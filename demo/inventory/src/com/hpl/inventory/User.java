package com.hpl.inventory;


public class User implements Runnable {
	private static final String defaultInventoryName = "Inventory1";
	private String inventoryName;
    private Inventory inventory;
    
    public User() {
    	this.inventoryName = defaultInventoryName;
    }
    
    public User(String inventoryName) {
    	this.inventoryName = inventoryName;
    }

    public void lookupInventory(String inventoryName) {
    	//inventory = NameService.lookup(Inventory.TYPE, inventoryName);
        inventory = Inventory.TYPE.lookupName(inventoryName);
    	if (inventory == null) throw new NullPointerException();
    }

    public void printInventory() {
    	System.out.println("\nInventory: short report");
    	inventory.print();
    	
    	System.out.println("\n\nInventory: long report");
    	inventory.printAll();
    }
    
    
    public void printReport(String name) {
    	Report report = inventory.generateReport(name);
    	report.print();
    }


    public void run() {
    	lookupInventory(inventoryName);
    	printInventory();
    	printReport("Analysis Report");
    	System.out.println("com.hpl.inventory.User complete");
    }
    
    public static void main(String[] args) {
    	if (args.length == 0) {
    		throw new NullPointerException(
    			"Error: No inventoryName specified\nUsage: com.hpl.inventory.User <inventoryName>");
    	}
        String inventoryName = args[0];
        new User(inventoryName).run();
    }


    // load C++ library to use MDS
    static {
//    	System.loadLibrary("libmds-jni");  // MDS Java API JNI plus MDS Core  // Windows
    	System.loadLibrary("mds-jni");  // MDS Java API JNI plus MDS Core  // Linux
    }	

}
