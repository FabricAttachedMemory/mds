/**
 * TaskListExecutor 
 * Given a list of Runnable tasks, executes them all in a thread pool
 *
 * author: suspence
 */



package com.hpl.inventory;

import java.util.List;
import java.util.concurrent.Executors;
import java.util.concurrent.ExecutorService;


public class TaskListExecutor implements Runnable {

    private final ExecutorService pool;
    private final List<Runnable> taskList;

    public TaskListExecutor(int poolSize, List<Runnable> taskList) {
        this.pool = Executors.newFixedThreadPool(poolSize);
        this.taskList = taskList;
    }

    public void run() {
        for (Runnable task : taskList) {
            pool.execute( task ); 
        }

        // orderly shutdown - previously submitted tasks are executed
        pool.shutdown();
    }

} // end TaskListExecutor 


