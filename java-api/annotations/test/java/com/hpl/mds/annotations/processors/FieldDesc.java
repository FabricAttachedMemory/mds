package com.hpl.mds.annotations.processors;

import java.util.Arrays;
import java.util.List;

public class FieldDesc {

	private String type;
	private List<String> typeParams;
	private String name;
	private String creationMethod;
	private List<String> methodArgs;

	public FieldDesc(String type, String typeParam, String name,
			String createionMethod, String mdsType) {
		setFields(type, name, createionMethod);
		this.typeParams = Arrays.asList(typeParam);
		this.methodArgs = Arrays.asList(mdsType);
	}

	public FieldDesc(String type, String name, String createionMethod) {
		setFields(type, name, createionMethod);
	}
	
	private void setFields(String type, String name,
			String createionMethod) {
		this.type = type;
		this.name = name;
		this.creationMethod = createionMethod;
	}
	
	public void setTypeParams(String... typeParams) {
		this.typeParams = Arrays.asList(typeParams);
	}
	
	public void setMethodArgs(String... methodArgs) {
		this.methodArgs = Arrays.asList(methodArgs);
	}

	public String getType() {
		return type;
	}

	public String getName() {
		return name;
	}

	public String getCreationMethod() {
		return creationMethod;
	}

	public List<String> getMethodArgs() {
		return methodArgs;
	}

	public List<String> getTypeParams() {
		return typeParams;
	}

}
