package com.hpl.mds.annotations.processors;

import static org.junit.Assert.*;

import java.io.ByteArrayOutputStream;

import org.junit.Test;

/**
 * Integration tests for MDS Annotation Processor
 */
public class AnnotationProcessorShouldFailTest {

	private static final String PATH = "com/hpl/mds/test/invalid/";
	
	private static final JCompiler COMPILER = JCompiler.getInstance();


    @Test
    public void shouldNotProcessRecordWithoutSuffix() {
        // ignore schema if name collision
        testInvalidRecord("RecordSchemaWithoutSuffix.java");
    }

	@Test
	public void shouldNotProcessClass() {
		testInvalidRecord("RecordSchemaClass.java");
	}

	private void testInvalidRecord(String javaSrcFile) {
		ByteArrayOutputStream err = new ByteArrayOutputStream();
		COMPILER.compileSchema(err, PATH + javaSrcFile);
		String errLog = err.toString();
		System.err.print(errLog);
		assertTrue(errLog.contains("Ignoring Schema"));
	}

}
