package com.hpl.mds.test.valid;

import com.hpl.mds.annotations.Private;
import com.hpl.mds.annotations.Protected;
import com.hpl.mds.annotations.RecordSchema;

@RecordSchema
public interface MethodsInstanceSchema {

    static String twoParams(MethodsInstance.Private self, String prefix, String suffix) {
        return prefix + self.toString() + suffix;
    }

    static String primitiveParam(MethodsInstance.Private self, int val) {
        return self.toString() + val;
    }

    static String recordParam(MethodsInstance.Private self, MethodsInstance record) {
        return record.toString() + self.toString();
    }

    static void voidReturn(MethodsInstance.Private self, int val) {
        Integer.valueOf(val).toString();
    }

    static int primitiveReturn(MethodsInstance.Private self, int val) {
        return val + 1;
    }

    static MethodsInstance recordReturn(MethodsInstance.Private self, int val) {
        return self;
    }

    @Protected
    static int protectedMethod(MethodsInstance.Private self, int val) {
        return val + 1;
    }

    @Private
    static int privateMethod(MethodsInstance.Private self, int val) {
        return val + 1;
    }
}
