package com.hpl.mds.annotations.processor.parser;

import java.util.Map;

import com.hpl.mds.annotations.Emitted;
import com.hpl.mds.annotations.processor.RecordInfo;
import com.hpl.mds.annotations.processor.RecordInfo.GetterFormatInfo;
import com.hpl.mds.annotations.processor.RecordInfo.Visibility;

/**
 * Stores the information of a schema
 * 
 * @author Abraham Alcantara
 */
public class SchemaContext {

    /**
     * simple name of the record
     */
    private String recordSimpleName;

    /**
     * qualified name of the record
     */
    private String recordName;

    /**
     * information of the parent record
     */
    private RecordInfo parent;

    /**
     * collection of visibilities of the given schema
     */
    private Map<Emitted, Visibility> visibilities;

    /**
     * getter name format for this schema
     */
    private GetterFormatInfo getterNameFormat;

    /**
     * package of the managed record
     */
    private String pkg;

    public void setGetterNameFormat(GetterFormatInfo getterNameFormat) {
        this.getterNameFormat = getterNameFormat;
    }

    public GetterFormatInfo getGetterNameFormat() {
        return getterNameFormat;
    }

    public void setParent(RecordInfo parent) {
        this.parent = parent;
    }

    public RecordInfo getParent() {
        return parent;
    }

    public void setRecordName(String recordName) {
        this.recordName = recordName;
    }

    public String getRecordName() {
        return recordName;
    }

    public void setVisibilities(Map<Emitted, Visibility> visibilities) {
        this.visibilities = visibilities;
    }

    public Map<Emitted, Visibility> getVisibilities() {
        return visibilities;
    }

    public void setRecordSimpleName(String recordSimpleName) {
        this.recordSimpleName = recordSimpleName;
    }

    public String getRecordSimpleName() {
        return recordSimpleName;
    }

    public void setPkg(String pkg) {
        this.pkg = pkg;
    }

    public String getPkg() {
        return pkg;
    }

}