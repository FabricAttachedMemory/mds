package com.hpl.mds;

import java.io.File;

import org.apache.log4j.Logger;

public class NativeLibraryLoader {

  private static final Logger LOGGER = Logger.getLogger(NativeLibraryLoader.class);

  private static final String DEFAULT_LIB_NAME = "mds-jni";
  private static final String PROPERTY_LIB_PATH = "mds.jni.library.path";

  private static final NativeLibraryLoader INSTANCE = new NativeLibraryLoader();

  private NativeLibraryLoader() {
    String libPath = System.getProperty(PROPERTY_LIB_PATH);
    if (libPath != null) {
      try {
        System.load(libPath);
      } catch (Error e) {
        LOGGER.error("MDS native library couldn't be found using the given path:" + libPath, e);
      }
    } else {
      try {
        System.loadLibrary(DEFAULT_LIB_NAME);
      } catch (Error e) {
        String javaLibraryPaht = System.getProperty("java.library.path"); 
        String defaultPath = new File(javaLibraryPaht, System.mapLibraryName(DEFAULT_LIB_NAME)).getAbsolutePath();
        LOGGER.error("MDS native library couldn't be found using the default path:" + defaultPath, e);
      }
    }
  }

  public static NativeLibraryLoader getInstance() {
    return INSTANCE;
  }

}
