package com.hpl.mds.task;

import java.util.HashSet;
import java.util.Set;

import com.hpl.mds.task.Task;


public class TaskContainer {

    // public static enum Operation {
    //     READ, WRITE
    // }

    public static TaskContainer setTaskContainer(Task task, Operation operation) {
        switch (operation) {
            case WRITE: 
                return new WriteTaskContainer(task);
            default: // READ
                return new TaskContainer(task);
        }
    }

    private Set<Task> allTasks;

    public TaskContainer(Task task) {
        allTasks = new HashSet<>();
        addTask(task);
    }

    public void addTask(Task task) {
        allTasks.add(task);
    }

    public Set<Task> allTasks() {
        return allTasks;
    }

} // end class TaskContainer

