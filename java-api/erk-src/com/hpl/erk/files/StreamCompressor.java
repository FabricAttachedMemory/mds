package com.hpl.erk.files;

import java.io.BufferedOutputStream;
import java.io.File;
import java.io.FileOutputStream;
import java.io.IOException;
import java.io.OutputStream;
import java.lang.reflect.Constructor;
import java.lang.reflect.InvocationTargetException;
import java.util.HashMap;
import java.util.Map;
import java.util.zip.GZIPOutputStream;
import java.util.zip.ZipOutputStream;

import org.apache.commons.compress.compressors.bzip2.BZip2CompressorOutputStream;

public abstract class StreamCompressor {
  private static Map<String, StreamCompressor> decompressors = new HashMap<>();

  public static final StreamCompressor UNCOMPRESSED = new StreamCompressor() {
    @Override
    public OutputStream compress(OutputStream s) {
      return s;
    }
  };
  
  public static final StreamCompressor GZIP = register(GZIPOutputStream.class, "gz", "GZ");
  public static final StreamCompressor ZIP = register(ZipOutputStream.class, "zip", "ZIP");
  public static final StreamCompressor BZIP2 = register(BZip2CompressorOutputStream.class, "bz2", "BZ2");
      
  
  
  private static class Providing<T extends OutputStream> extends StreamCompressor {
    final Class<T> clss;
    Constructor<T> ctor;
    
    public Providing(Class<T> clss) {
      this.clss = clss;
    }

    @Override
    public OutputStream compress(OutputStream s) throws IOException {
      try {
        if (ctor == null) {
          ctor = clss.getConstructor(OutputStream.class);
        }
        s = ctor.newInstance(s);
        s = new BufferedOutputStream(s);
        return s;
      } catch (InstantiationException | IllegalAccessException
               | IllegalArgumentException | InvocationTargetException | NoSuchMethodException | SecurityException e) {
        throw new IOException(String.format("Error trying to create decompress stream for class %s", clss));
      }
    }
    
  }
  
  
  
  public abstract OutputStream compress(OutputStream s) throws IOException;
  public StreamCompressor register(String... exts) {
    for (String ext : exts) {
      decompressors.put("."+ext, this);
    }
    return this;
  }
  
  public static <T extends OutputStream> StreamCompressor register(Class<T> clss, String ...exts) {
    return new Providing<>(clss).register(exts);
  }
  
  public static OutputStream compressTo(File file) throws IOException {
    StreamCompressor compressor = null;
    final String outfileName = file.getName();
    int longestLength = 0;
    for (String ext : decompressors.keySet()) {
      final int len = ext.length();
      if (len > longestLength && outfileName.endsWith(ext)) {
        compressor = decompressors.get(ext);
        longestLength = len;
      }
    }
    if (compressor == null) {
      compressor = UNCOMPRESSED;
    }
    OutputStream s = new FileOutputStream(file);
    s = compressor.compress(s);
    return s;
  }
  
}
