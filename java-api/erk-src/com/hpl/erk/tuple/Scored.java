package com.hpl.erk.tuple;

/** 
 * A simple tuple/pair to keep track of a score with an item: (double,object).
 * Comparable on the score.
 * @author George.Forman@hp.com  (gforman)
 */
public final class Scored<T> implements Comparable<Scored<T>> {

	public double score;
	public T item;
	
	public Scored(){}
	
	public Scored(double score, T item) {
		this.score = score;
		this.item = item;
	}

	@Override
	public int compareTo(Scored<T> o) {
		return Double.compare(score, o.score);
	}
}
