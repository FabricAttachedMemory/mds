package com.hpl.erk.config.type;

import java.util.Collections;
import java.util.List;

import com.hpl.erk.config.GenericType;
import com.hpl.erk.config.PType;
import com.hpl.erk.config.TypeCache.Factory;
import com.hpl.erk.types.GenericTypeToken;
import com.hpl.erk.types.TypeToken;

public class TwoArgCompositeType<T, P1, P2> extends CompositeType<T> {
  protected final PType<P1> p1Type;
  protected final PType<P2> p2Type;
  
  public static class Generic<GT, B1, B2> extends GenericType<GT> {
    public Generic(GenericTypeToken generic) {
      super(generic);
      if (generic.arity() != 2) {
        throw new IllegalArgumentException(String.format("%s arity != 2", generic));
      }
    }
    
    public <P1 extends B1, P2 extends B2, T extends GT> PType<T> of(Sig<T> sig, final PType<P1> p1Type, final PType<P2> p2Type) {
      return TwoArgCompositeType.of(sig, this, p1Type, p2Type);  
    }

    public <T extends GT> PType<T> concreteType(Sig<T> sig, PType<?>...bounds) {
      if (bounds.length != 2) {
        throw new IllegalArgumentException(String.format("Wrong number of arguments (%,d) for %s", bounds.length, this));
      }
      @SuppressWarnings("unchecked")
      PType<? extends B1> b1 = (PType<? extends B1>)bounds[0];
      @SuppressWarnings("unchecked")
      PType<? extends B2> b2 = (PType<? extends B2>)bounds[1];
      return this.of(sig, b1, b2);
    }
    
    public final <T extends GT> PType<T> makeNew(Sig<T> sig, TypeToken token, PType<?>... bounds) {
      @SuppressWarnings("unchecked")
      PType<? extends B1> b1 = (PType<? extends B1>)bounds[0];
      @SuppressWarnings("unchecked")
      PType<? extends B2> b2 = (PType<? extends B2>)bounds[1];
      return makeNewType(sig, token, b1, b2);
    }

    public <T extends GT, P1 extends B1, P2 extends B2> 
    PType<T> makeNewType(Sig<T> sig, TypeToken token, PType<P1> p1Type, PType<P2> p2Type) {
      return new TwoArgCompositeType<>(this, token, p1Type, p2Type);
    }
    
    
  }
  
  protected TwoArgCompositeType(GenericType<? super T> generic,
                                TypeToken typeToken,
                                PType<P1> p1Type,
                                PType<P2> p2Type)
  {
    super(generic, typeToken);
    this.p1Type = p1Type;
    this.p2Type = p2Type;
  }

  @Override
  public List<PType<?>> bindings() {
    return Collections.<PType<?>>singletonList(p1Type);
  }
  
  public static <P1,P2,T> TwoArgCompositeType<T,P1,P2> of(final Sig<T> sig, final GenericType<? super T> generic, final PType<P1> p1Type, final PType<P2> p2Type) {
    final TypeToken token = generic.token(p1Type, p2Type);
    Factory<T> factory = new Factory<T>() {
      @Override
      public PType<T> create() {
        return generic.makeNew(sig, token, p1Type, p2Type);
      }
    };
    return (TwoArgCompositeType<T, P1,P2>)PType.of(token, factory);  

  }

}
