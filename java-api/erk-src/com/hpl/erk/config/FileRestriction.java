package com.hpl.erk.config;

import java.io.File;
import java.nio.file.Path;

import com.hpl.erk.config.ex.IllegalValueException;


public class FileRestriction  {
  public static Restriction<File> exists() {
    return new Restriction<File>() {
      @Override
      public boolean checkVal(File val) {
        return val.exists();
      }
      
      @Override
      public String describe() {
        return "Must exist";
      }
    };
  }
  public static Restriction<File> is_new() {
    return new Restriction<File>() {
      @Override
      public boolean checkVal(File val) {
        return val == null || !val.exists();
      }
      
      @Override
      public String describe() {
        return "Must not exist";
      }
    };
  }
  public static Restriction<File> is_dir() {
    return new Restriction<File>() {
      @Override
      public boolean checkVal(File val) {
        return val == null || val.isDirectory();
      }
      
      @Override
      public String describe() {
        return "Must be a directory";
      }
    };
  }
  
  public static final Interpreter<File> in_dir(final Path dir) {
    return in_dir(dir.toFile());
  }
  public static final Interpreter<File> in_dir(final File dir) {
    return new Interpreter<File>() {
      @Override
      public File interpret(File val) throws IllegalValueException {
        if (val == null) {
          return null;
        }
        return new File(dir, val.getPath());
      }
      
      @Override
      public String describe() {
        return String.format("In directory %s", dir);
      }
    };
  }

}