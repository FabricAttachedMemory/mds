package com.hpl.erk.chain;

import java.util.List;

public class RAListHead<Out> extends CollectionHead<Out> {
  private final List<? extends Out> list;
  
  protected <X extends Out> RAListHead(final List<X> list) {
    super(list);
    this.list = list;
  }
  
  @Override
  public RandomAccessSource<Out> randomAccess() {
    return new RandomAccessSource<Out>() {
      @Override
      public int size() {
        return list.size();
      }

      @Override
      public Out get(int i) {
        return list.get(i);
      }};
  }


  
}
