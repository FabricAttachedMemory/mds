package com.hpl.erk.chain;

import java.util.concurrent.ThreadLocalRandom;

import com.hpl.erk.chain.exceptions.RandomAccessSourceRequired;
import com.hpl.erk.func.SourceExhausted;

public class WithReplacementLink<Head, Out> extends Link<Head, Out, Out> {
  final int max;

  protected WithReplacementLink(Chain<Head, ? extends Out> pred, int max) {
    super(pred);
    this.max = max;
  }

  @Override
  public Flow pipeInto(final Receiver<? super Out> sink) {
    final RandomAccessSource<? extends Out> source = pred.randomAccess();
    if (source == null) {
      throw new RandomAccessSourceRequired();
    }
    return new Flow() {
      final int size = source.size();
      @Override
      public void perform() {
        for (int i=0; i<max; i++) {
          int which = ThreadLocalRandom.current().nextInt(size);
          if (!sink.receive(source.get(which))) {
            return;
          }
        }
        sink.close();
      }
    };
  }
  
  @Override
  public int expectedSize() {
    return max;
  }

  @Override
  public Context createContext() {
    final RandomAccessSource<? extends Out> raSource = pred.randomAccess();
    if (raSource == null) {
      throw new RandomAccessSourceRequired();
    }
    final int size = raSource.size();
    return new Context() {
      int i = 0;
      @Override
      public Out produce() throws SourceExhausted {
        exhaustedIf(i > max);
        int which = ThreadLocalRandom.current().nextInt(size);
        i++;
        return raSource.get(which);
      }
    };
  }
}
