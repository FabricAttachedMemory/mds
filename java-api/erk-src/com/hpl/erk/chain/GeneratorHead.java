package com.hpl.erk.chain;

import com.hpl.erk.func.NullaryFunc;
import com.hpl.erk.func.Generator;
import com.hpl.erk.func.SourceExhausted;

public class GeneratorHead<Out> extends ChainHead<Out> {
  protected final NullaryFunc<? extends Generator<? extends Out>> creator;
  protected final int expected;

  protected GeneratorHead(NullaryFunc<? extends Generator<? extends Out>> creator, int expected) {
    this.creator = creator;
    this.expected = expected;
  }

  protected GeneratorHead(NullaryFunc<? extends Generator<? extends Out>> creator) {
  	this(creator, Integer.MAX_VALUE);
  }
  
  @Override
  public int expectedSize() {
    return expected;
  }
  
  @Override
  public Flow pipeInto(final Receiver<? super Out> sink) {
    return new Flow() {
      final Generator<? extends Out> generator = creator.call();
      @Override
      public void perform() {
        try {
          while (true) {
            if (!sink.receive(generator.generate())) {
              return;
            }
          }
        } catch (SourceExhausted e) {
          sink.close();
        }
      }
    };
  }
  
  
  @Override
  public Context createContext() {
    return new Context() {
      protected final Generator<? extends Out> generator = creator.call();
      
      @Override
      public Out produce() throws SourceExhausted {
        return generator.generate();
      }
    };
  }
}
