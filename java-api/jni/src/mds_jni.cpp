void
mds::jni::stubNotImplemented(JNIEnv *env) {
  static jclass c = find_class(env, "com/hpl/mds/impl/Stub");
  if (c == nullptr) {
    return; // exception thrown
  }
  static jmethodID m = find_method(env, c, "notImplemented", "()V");
  if (m == nullptr) {
    return; // exception thrown
  }
  env->CallStaticObjectMethod(c, m);
}
